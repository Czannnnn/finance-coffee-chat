/*
 * gen-static-ipo.mjs — 정적 SEO 프리렌더 생성기 (수동 실행용, 빌드 파이프라인 아님)
 *
 * 목적: ipo.html 의 #ipoList 컨테이너는 gviz(JS)로만 채워져 초기 HTML 이 비어 있어
 *       JS 미실행 크롤러(특히 네이버 Yeti)에 공모주 데이터가 색인되지 않는다.
 *       이 스크립트는 현재 Google Sheets 데이터를 받아 정적 카드 마크업을 생성해
 *       #ipoList 에 시드(seed)로 주입한다. 사용자 브라우저는 기존 JS 가
 *       renderIPOList() 로 innerHTML 을 라이브 데이터로 교체(하이드레이션)한다.
 *
 * 재생성: node scripts/gen-static-ipo.mjs   (markers 사이만 갱신 — 멱등)
 *   - 기본은 라이브 gviz fetch. 실패 시 ./_gviz_raw.txt 캐시 사용.
 *
 * 주의: DART OpenDart 단일 출처 정책 — 실제 공시 데이터만 사용(샘플/익명 금지).
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SHEET_ID = '1_xXYdZ5d8COXBajVZ_tyd2p5JaOhQcQnNPQrS9-e-Jk';
const SHEET_NAME = 'normalized';
const HTML_PATH = join(ROOT, 'ipo.html');
const START = '<!--STATIC_IPO_START (auto-generated by scripts/gen-static-ipo.mjs — do not edit by hand)-->';
const END = '<!--STATIC_IPO_END-->';

const esc = (s) => String(s == null ? '' : s)
  .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

async function getRaw() {
  const url = `https://docs.google.com/spreadsheets/d/${SHEET_ID}/gviz/tq?tqx=out:json&sheet=${encodeURIComponent(SHEET_NAME)}`;
  try {
    const res = await fetch(url);
    const text = await res.text();
    if (text.includes('setResponse(')) { writeFileSync(join(ROOT, '_gviz_raw.txt'), text); return text; }
    throw new Error('unexpected response');
  } catch (e) {
    const cache = join(ROOT, '_gviz_raw.txt');
    if (existsSync(cache)) { console.warn('live fetch 실패 → 캐시 사용:', e.message); return readFileSync(cache, 'utf8'); }
    throw e;
  }
}

function parse(text) {
  const json = JSON.parse(text.match(/google\.visualization\.Query\.setResponse\(([\s\S]*)\);?/)[1]);
  const val = (c, i) => { if (!c[i]) return ''; return c[i].f ? String(c[i].f) : (c[i].v != null ? String(c[i].v) : ''); };
  const dateVal = (c, i) => {
    if (!c[i] || c[i].v == null) return '';
    const raw = String(c[i].v);
    const dm = raw.match(/Date\((\d+),(\d+),(\d+)\)/);
    if (dm) return `${dm[1]}-${String(Number(dm[2]) + 1).padStart(2, '0')}-${dm[3].padStart(2, '0')}`;
    if (/^\d{4}-\d{2}-\d{2}$/.test(raw)) return raw;
    if (c[i].f) { const fm = String(c[i].f).match(/(\d{4})[-.](\d{1,2})[-.](\d{1,2})/); if (fm) return `${fm[1]}-${fm[2].padStart(2, '0')}-${fm[3].padStart(2, '0')}`; }
    return raw;
  };
  return json.table.rows.map((row) => {
    const c = row.c;
    const confirmedPriceRaw = val(c, 4), bandLow = val(c, 5), bandHigh = val(c, 6);
    const priceRange = (bandLow && bandHigh) ? `${bandLow}~${bandHigh}원` : (confirmedPriceRaw ? `${confirmedPriceRaw}원` : '-');
    return {
      name: val(c, 1), market: val(c, 2) || 'kosdaq', status: val(c, 3) || 'upcoming',
      price: confirmedPriceRaw ? `${confirmedPriceRaw}원` : '-', priceRange,
      bandPosition: val(c, 7), lead: val(c, 12), competition: val(c, 13) || '-',
      competitionNote: (val(c, 14) || '').split(' · ').filter(Boolean).find((p) => !p.startsWith('⚠')) || '기관 수요예측 경쟁률',
      lockup: val(c, 15) || '-',
      subscribe: [dateVal(c, 8), dateVal(c, 9)], refund: dateVal(c, 10), listing: dateVal(c, 11),
    };
  }).filter((x) => x.name && x.subscribe[0]);
}

const toD = (s) => (/^\d{4}-\d{2}-\d{2}$/.test(s) ? new Date(s + 'T00:00:00+09:00') : null);
const fmt = (s) => (/^\d{4}-\d{2}-\d{2}$/.test(s) ? s.replace(/-/g, '.') : (s || '-'));

function statusInfo(ipo, today) {
  const s0 = toD(ipo.subscribe[0]), s1 = toD(ipo.subscribe[1]), ls = toD(ipo.listing);
  if (ls && ls <= today) return { key: 'completed', label: '상장완료' };
  if (s0 && s1 && s0 <= today && today <= s1) return { key: 'active', label: '청약중' };
  if (s0 && today < s0) return { key: 'upcoming', label: '청약예정' };
  return { key: 'pending_listing', label: '상장대기' };
}

function card(ipo, today) {
  const st = statusInfo(ipo, today);
  const market = ipo.market === 'kospi' ? '코스피' : '코스닥';
  const price = ipo.price !== '-' ? ipo.price : ipo.priceRange;
  const band = ipo.bandPosition ? ` <small style="color:var(--medium-roast);font-weight:500;">[${esc(ipo.bandPosition)}]</small>` : '';
  return `
        <div class="ipo-card">
          <div class="ipo-top">
            <div class="ipo-name-group">
              <div class="ipo-name">${esc(ipo.name)}</div>
              <div class="ipo-badges">
                <span class="ipo-badge ${ipo.market === 'kospi' ? 'kospi' : 'kosdaq'}">${market}</span>
                <span class="ipo-badge ${st.key}">${st.label}</span>
              </div>
            </div>
          </div>
          <div class="ipo-timeline">
            <div class="tl-step"><div class="tl-label">청약</div><div class="tl-date">${fmt(ipo.subscribe[0])}~${fmt(ipo.subscribe[1])}</div></div>
            <div class="tl-step"><div class="tl-label">환불</div><div class="tl-date">${fmt(ipo.refund)}</div></div>
            <div class="tl-step"><div class="tl-label">상장</div><div class="tl-date">${fmt(ipo.listing)}</div></div>
          </div>
          <div class="ipo-info">
            <div class="info-item"><span class="info-label">공모가</span><span class="info-value">${esc(price)}${band}</span></div>
            <div class="info-item"><span class="info-label">주관사</span><span class="info-value">${esc(ipo.lead || '-')}</span></div>
            <div class="info-item"><span class="info-label">${esc(ipo.competitionNote)}</span><span class="info-value">${esc(ipo.competition)}</span></div>
            <div class="info-item"><span class="info-label">의무보유확약</span><span class="info-value">${esc(ipo.lockup)}</span></div>
          </div>
          <a href="/ipo-analysis?name=${encodeURIComponent(ipo.name)}" class="detail-btn">&#128270; 세부 분석 보러가기</a>
        </div>`;
}

async function main() {
  const today = new Date();
  const data = parse(await getRaw());
  // 정렬: 청약예정·청약중 먼저(청약일 오름차순), 그 다음 상장대기, 마지막 상장완료(상장일 내림차순)
  const rank = { upcoming: 0, active: 0, pending_listing: 1, completed: 2 };
  const enriched = data.map((d) => ({ d, st: statusInfo(d, today) }));
  enriched.sort((a, b) => {
    const ra = rank[a.st.key], rb = rank[b.st.key];
    if (ra !== rb) return ra - rb;
    if (rb === 2) return (b.d.listing || '').localeCompare(a.d.listing || '');
    return (a.d.subscribe[0] || '').localeCompare(b.d.subscribe[0] || '');
  });
  const picked = enriched.slice(0, 24).map((x) => x.d);
  const cards = picked.map((d) => card(d, today)).join('');
  const block = `${START}${cards}\n        ${END}`;

  let html = readFileSync(HTML_PATH, 'utf8');
  const empty = '<div class="ipo-list" id="ipoList"></div>';
  const reInjected = /<div class="ipo-list" id="ipoList">[\s\S]*?<!--STATIC_IPO_END--><\/div>/;
  if (reInjected.test(html)) {
    html = html.replace(reInjected, `<div class="ipo-list" id="ipoList">${block}</div>`);
  } else if (html.includes(empty)) {
    html = html.replace(empty, `<div class="ipo-list" id="ipoList">${block}</div>`);
  } else {
    throw new Error('주입 지점(#ipoList)을 찾지 못함');
  }
  writeFileSync(HTML_PATH, html);
  console.log(`주입 완료: ${picked.length}건 (전체 ${data.length}건 중)`);
  console.log('상위 표본:', picked.slice(0, 8).map((d) => `${d.name}[${statusInfo(d, today).label}]`).join(', '));
}
main().catch((e) => { console.error('실패:', e); process.exit(1); });
