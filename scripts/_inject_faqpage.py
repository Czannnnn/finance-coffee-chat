def main() -> None:
    inserted: list[str] = []
    for slug, qas in FAQS.items():
        fp = BASE / f"{slug}.html"
        if not fp.exists():
            print(f"SKIP (missing): {fp}")
            continue
        text = fp.read_text(encoding="utf-8")
        if '"@type": "FAQPage"' in text:
            print(f"SKIP (already has FAQPage): {slug}")
            continue

        faqpage = {
            "@context": "https://schema.org",
            "@type": "FAQPage",
            "name": f"{SECTION_TITLE[slug]} 자주 묻는 질문",
            "inLanguage": "ko",
            "url": f"{BASE_URL}{slug}",
            "mainEntity": [
                {
                    "@type": "Question",
                    "name": q,
                    "acceptedAnswer": {"@type": "Answer", "text": a},
                }
                for q, a in qas
            ],
        }
        block_body = json.dumps(faqpage, ensure_ascii=False, indent=2)
        block = (
            '  <script type="application/ld+json">\n  '
            + block_body.replace("\n", "\n  ")
            + "\n  </script>"
        )

        pattern = r'("@type": "BreadcrumbList"[\s\S]*?</script>)'
        new_text, n = re.subn(pattern, r"\1\n" + block, text, count=1)
        if n == 0:
            print(f"SKIP (no Breadcrumb anchor): {slug}")
            continue
        fp.write_text(new_text, encoding="utf-8")
        inserted.append(slug)

    print(f"\nInserted FAQPage into {len(inserted)} files:")
    for s in inserted:
        print(f"  {s}")


if __name__ == "__main__":
    main()
