/*
 * gen-static-ipo-analysis.mjs — ipo-analysis.html 정적 SEO 프리렌더 생성기
 *
 * 목적: ipo-analysis.html 의 #analysisContainer 는 gviz(JS)로만 채워져 초기 HTML 이 비어 있어
 *       JS 미실행 크롤러(네이버 Yeti 등)에 분석 종목·등급이 색인되지 않는다.
 *       이 스크립트는 현재 Google Sheets 데이터를 받아 등급(S~D)을 계산하고 정적 분석 카드를
 *       #analysisContainer 에 시드(seed)로 주입한다. 사용자 브라우저는 기존 JS 가 render() 로
 *       innerHTML 을 라이브 데이터로 교체(하이드레이션)한다.
 *
 * 재생성: node scripts/gen-static-ipo-analysis.mjs   (markers 사이만 갱신 — 멱등)
 *   - 기본은 라이브 gviz fetch. 실패 시 ./_gviz_raw.txt 캐시 사용.
 *
 * 등급 로직·컬럼 매핑은 ipo-analysis.html 의 evaluate()/calcGrade()/loadFromSheets() 와 동일하게 유지.
 * 주의: DART OpenDart 단일 출처 정책 — 실데이터만 사용(SAMPLE_DATA·가공 데이터 금지).
 */
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const SHEET_ID = '1_xXYdZ5d8COXBajVZ_tyd2p5JaOhQcQnNPQrS9-e-Jk';
const SHEET_NAME = 'normalized';
const HTML_PATH = join(ROOT, 'ipo-analysis.html');
const START = '<!--STATIC_ANALYSIS_START (auto-generated by scripts/gen-static-ipo-analysis.mjs — do not edit by hand)-->';
const END = '<!--STATIC_ANALYSIS_END-->';
const MAX_CARDS = 24;

const esc = (s) => String(s == null ? '' : s)
  .replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

async function getRaw() {
  const url = `https://docs.google.com/spreadsheets/d/${SHEET_ID}/gviz/tq?tqx=out:json&sheet=${encodeURIComponent(SHEET_NAME)}`;
  try {
    const res = await fetch(url);
    const text = await res.text();
    if (text.includes('setResponse(')) { writeFileSync(join(ROOT, '_gviz_raw.txt'), text); return text; }
    throw new Error('unexpected response');
  } catch (e) {
    const cache = join(ROOT, '_gviz_raw.txt');
    if (existsSync(cache)) { console.warn('live fetch 실패 → 캐시 사용:', e.message); return readFileSync(cache, 'utf8'); }
    throw e;
  }
}

// ipo-analysis.html loadFromSheets() 와 동일한 컬럼 매핑 (v2 normalized 시트)
function parse(text) {
  const json = JSON.parse(text.match(/google\.visualization\.Query\.setResponse\(([\s\S]*)\);?/)[1]);
  return json.table.rows.map((row) => {
    const c = row.c;
    const val = (i) => { if (!c[i]) return ''; return c[i].f ? String(c[i].f) : (c[i].v != null ? String(c[i].v) : ''); };
    const dateVal = (i) => {
      if (!c[i] || c[i].v == null) return '';
      const raw = String(c[i].v);
      const dm = raw.match(/Date\((\d+),(\d+),(\d+)\)/);
      if (dm) return `${dm[1]}-${String(Number(dm[2]) + 1).padStart(2, '0')}-${dm[3].padStart(2, '0')}`;
      if (/^\d{4}-\d{2}-\d{2}$/.test(raw)) return raw;
      if (c[i].f) { const fm = String(c[i].f).match(/(\d{4})[-.](\d{1,2})[-.](\d{1,2})/); if (fm) return `${fm[1]}-${fm[2].padStart(2, '0')}-${fm[3].padStart(2, '0')}`; }
      return raw;
    };
    const confirmedPrice = val(4), bandLow = val(5), bandHigh = val(6);
    const expectedPrice = (bandLow && bandHigh) ? `${bandLow}~${bandHigh}` : '';
    const competitionStr = val(13), lockupStr = val(15), floatStr = val(17);
    const compMatch = competitionStr.match(/([\d,.]+)\s*:/);
    const competitionNum = compMatch ? parseFloat(compMatch[1].replace(/,/g, '')) : 0;
    let lockupNum = parseFloat(lockupStr.replace(/[^0-9.]/g, '')) || 0;
    if (lockupNum > 0 && lockupNum < 1) lockupNum = Math.round(lockupNum * 10000) / 100;
    let floatRatioNum = parseFloat(floatStr.replace(/[^0-9.]/g, '')) || 0;
    if (floatRatioNum > 0 && floatRatioNum < 1) floatRatioNum = Math.round(floatRatioNum * 10000) / 100;
    const priceBandTop = bandHigh ? parseInt(String(bandHigh).replace(/,/g, '')) || 0 : 0;
    const priceFixed = confirmedPrice ? parseInt(String(confirmedPrice).replace(/,/g, '')) || 0 : 0;
    return {
      name: val(1), market: val(2) || 'kosdaq', status: val(3) || 'upcoming',
      price: confirmedPrice ? `${confirmedPrice}원` : '-',
      priceRange: expectedPrice ? `${expectedPrice}원` : '-',
      priceBandTop, priceFixed,
      competition: competitionStr || '-', competitionNum,
      lockup: lockupNum > 0 ? `${lockupNum}%` : '-', lockupNum,
      floatRatio: floatRatioNum > 0 ? `${floatRatioNum}%` : '-', floatRatioNum,
      shares: val(18) || '-', lead: val(12),
      subscribe: [dateVal(8), dateVal(9)], refund: dateVal(10), listing: dateVal(11),
    };
  }).filter((x) => x.name && x.subscribe[0]);
}

// ── 등급 로직 (ipo-analysis.html evaluate()/calcGrade() 동일) ──
function evaluate(ipo) {
  return [
    { num: 1, name: '기관 경쟁률', value: ipo.competition, pass: ipo.competitionNum >= 800, threshold: '800:1↑', available: ipo.competitionNum > 0 },
    { num: 2, name: '확정 공모가', value: ipo.price, pass: ipo.priceFixed > 0 && ipo.priceFixed >= ipo.priceBandTop, threshold: '상단↑', available: ipo.priceFixed > 0 },
    { num: 3, name: '의무 확약', value: ipo.lockup, pass: ipo.lockupNum >= 15, threshold: '15%↑', available: ipo.lockupNum > 0 },
    { num: 4, name: '유통 물량', value: ipo.floatRatio, pass: ipo.floatRatioNum > 0 && ipo.floatRatioNum <= 25, threshold: '25%↓', available: ipo.floatRatioNum > 0 },
  ];
}
function calcGrade(ipo) {
  const conds = evaluate(ipo);
  const passCount = conds.filter((c) => c.pass).length;
  if (passCount === 4 && ipo.lockupNum >= 50) return 'S';
  if (passCount >= 3) return 'A';
  if (passCount >= 2) return 'B';
  if (passCount >= 1) return 'C';
  return 'D';
}
const GRADE_ORDER = ['S', 'A', 'B', 'C', 'D'];
function getGradeInfo(g) {
  return ({ S: '4대 조건 충족', A: '3개 이상 충족', B: '2개 충족', C: '1개 충족', D: '미충족' })[g] || '미충족';
}
function getStatusLabel(s) { return ({ upcoming: '청약예정', active: '청약중', completed: '상장완료' })[s] || s; }
function fmtDate(str) { if (!str || !/^\d{4}-\d{2}-\d{2}$/.test(str)) return '-'; const [, m, d] = str.split('-'); return `${parseInt(m)}/${parseInt(d)}`; }

function card(ipo) {
  const conds = evaluate(ipo).map((c) => {
    const cls = !c.available ? 'fail' : (c.pass ? 'pass' : 'fail');
    const status = !c.available ? '미확정' : (c.pass ? '충족' : '미충족');
    const icon = !c.available ? '&#8943;' : (c.pass ? '&#10003;' : '&#10007;');
    return `
              <div class="analysis-item ${cls}">
                <div class="analysis-label"><span class="num">${c.num}</span> ${esc(c.name)}</div>
                <div class="analysis-value">${esc(c.value)}</div>
                <div class="analysis-status">${icon} ${status}</div>
                <div class="analysis-threshold">기준: ${esc(c.threshold)}</div>
              </div>`;
  }).join('');
  const market = ipo.market === 'kospi' ? '코스피' : '코스닥';
  return `
            <div class="analysis-card">
              <div class="card-top">
                <div class="card-name-group">
                  <div class="card-name">${esc(ipo.name)}</div>
                  <div class="card-badges">
                    <span class="ipo-badge ${ipo.market === 'kospi' ? 'kospi' : 'kosdaq'}">${market}</span>
                    <span class="ipo-badge ${esc(ipo.status)}">${getStatusLabel(ipo.status)}</span>
                  </div>
                </div>
                <div class="card-grade ${ipo.grade}">${ipo.grade}</div>
              </div>
              <div class="analysis-grid">${conds}</div>
              <div class="card-info">
                <div class="info-item"><span class="info-label">청약일</span><span class="info-value">${fmtDate(ipo.subscribe[0])}~${fmtDate(ipo.subscribe[1])}</span></div>
                <div class="info-item"><span class="info-label">주관사</span><span class="info-value">${esc(ipo.lead || '-')}</span></div>
                <div class="info-item"><span class="info-label">공모가 밴드</span><span class="info-value">${esc(ipo.priceRange)}</span></div>
                <div class="info-item"><span class="info-label">공모주식수</span><span class="info-value">${esc(ipo.shares)}</span></div>
              </div>
            </div>`;
}

function buildBlock(picked, asOf) {
  const groups = {};
  GRADE_ORDER.forEach((g) => { groups[g] = []; });
  picked.forEach((ipo) => { if (groups[ipo.grade]) groups[ipo.grade].push(ipo); });
  let html = `\n        <p class="static-asof" style="font-size:12px;color:var(--medium-roast);margin-bottom:8px;">분석 기준일 ${asOf} · DART 공시 기준 · 페이지 로드 시 최신 데이터로 자동 갱신됩니다.</p>`;
  GRADE_ORDER.forEach((grade) => {
    const items = groups[grade];
    if (!items.length) return;
    html += `
        <div class="section-header">
          <div class="grade-badge ${grade}">${grade}</div>
          <span class="section-label">${grade}등급</span>
          <span class="section-sublabel">${getGradeInfo(grade)}</span>
          <span class="section-count">${items.length}건</span>
        </div>
        <div class="analysis-list">${items.map(card).join('')}</div>`;
  });
  return html;
}

async function main() {
  const data = parse(await getRaw());
  data.forEach((ipo) => { ipo.grade = calcGrade(ipo); });
  // 정렬: 등급 우선(S→D), 그 다음 청약일 오름차순
  data.sort((a, b) => {
    const ga = GRADE_ORDER.indexOf(a.grade), gb = GRADE_ORDER.indexOf(b.grade);
    if (ga !== gb) return ga - gb;
    return (a.subscribe[0] || '').localeCompare(b.subscribe[0] || '');
  });
  const picked = data.slice(0, MAX_CARDS);
  const d = new Date();
  const asOf = `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, '0')}-${String(d.getDate()).padStart(2, '0')}`;
  const block = `${START}${buildBlock(picked, asOf)}\n        ${END}`;

  let html = readFileSync(HTML_PATH, 'utf8');
  const empty = '<div id="analysisContainer"></div>';
  const reInjected = /<div id="analysisContainer">[\s\S]*?<!--STATIC_ANALYSIS_END--><\/div>/;
  if (reInjected.test(html)) {
    html = html.replace(reInjected, `<div id="analysisContainer">${block}</div>`);
  } else if (html.includes(empty)) {
    html = html.replace(empty, `<div id="analysisContainer">${block}</div>`);
  } else {
    throw new Error('주입 지점(#analysisContainer)을 찾지 못함');
  }
  writeFileSync(HTML_PATH, html);
  console.log(`주입 완료: ${picked.length}건 (전체 ${data.length}건 중, 기준일 ${asOf})`);
  const dist = GRADE_ORDER.map((g) => `${g}:${picked.filter((x) => x.grade === g).length}`).join(' ');
  console.log('등급 분포:', dist);
}
main().catch((e) => { console.error('실패:', e); process.exit(1); });
